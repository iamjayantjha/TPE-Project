package com.zerostic.goodmorning.Application;

public class Users {
    private String id;
    private String first_name;
    private String account_open_date;
    private String country;
    private String email;
    private String last_name;
    private String imageUri;
    private String phone_number;
    private String payment_status;
    private String verification;
    private String password;
    private String terms_of_service;

    public Users() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getAccount_open_date() {
        return account_open_date;
    }

    public void setAccount_open_date(String account_open_date) {
        this.account_open_date = account_open_date;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getImageUri() {
        return imageUri;
    }

    public void setImageUri(String imageUri) {
        this.imageUri = imageUri;
    }

    public String getPhone_number() {
        return phone_number;
    }

    public void setPhone_number(String phone_number) {
        this.phone_number = phone_number;
    }

    public String getPayment_status() {
        return payment_status;
    }

    public void setPayment_status(String payment_status) {
        this.payment_status = payment_status;
    }

    public String getVerification() {
        return verification;
    }

    public void setVerification(String verification) {
        this.verification = verification;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getTerms_of_service() {
        return terms_of_service;
    }

    public void setTerms_of_service(String terms_of_service) {
        this.terms_of_service = terms_of_service;
    }
}
